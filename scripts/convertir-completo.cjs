// Sort by Catalan length DESC (longest first to avoid partial replacement)
cat2es.sort((a, b) => b[0].length - a[0].length);

// Add single letter "i" (Catalan "and") → "y" at the end
cat2es.push([" i ", " y "]);
cat2es.push([" i\n", " y\n"]);
cat2es.push([", i ", ", y "]);
cat2es.push([". i ", ". y "]);
cat2es.push(["; i ", "; y "]);

const files = fs.readdirSync(dir).filter(f => f.endsWith('.json'));
let total = 0;
files.forEach(f => {
  let c = fs.readFileSync(dir+'/'+f, 'utf8');
  cat2es.forEach(([cat, es]) => {
    if (c.includes(cat)) {
      const parts = c.split(cat);
      total += parts.length - 1;
      c = parts.join(es);
    }
  });
  fs.writeFileSync(dir+'/'+f, c);
});

console.log(`✅ Conversión completa: ${total} reemplazos en ${files.length} archivos`);

// Verify no Catalan specific chars left
let remain = 0;
files.forEach(f => {
  const c = fs.readFileSync(dir+'/'+f, 'utf8');
  if (/[àèò]/.test(c)) remain++;
});
if (remain === 0) console.log('✅ Sin acentos catalanes');

