const files = fs.readdirSync(dir).filter(f => f.endsWith('.json'));
let total = 0;
files.forEach(f => {
  let c = fs.readFileSync(dir+'/'+f, 'utf8');
  dict.forEach(([re, replacement]) => {
    const before = c;
    c = c.replace(re, replacement);
    if (c !== before) total++;
  });
  fs.writeFileSync(dir+'/'+f, c);
  const data = JSON.parse(c);
  console.log(f+': '+data.cards.length+' cards');
});
console.log('\nTotal reemplazos: '+total);

// Verify
const leftover = [];
files.forEach(f => {
  const c = fs.readFileSync(dir+'/'+f, 'utf8');
  const m = c.match(/[àèò]/g);
  if (m) {
    const examples = c.match(/[a-zA-Z]*[àèò][a-zA-Z]*/g);
    if (examples) leftover.push(f+': '+examples.slice(0,5).join(', '));
  }
});
if (leftover.length === 0) console.log('✅ Sin acentos catalanes');
else leftover.forEach(l => console.log('⚠️ '+l));
